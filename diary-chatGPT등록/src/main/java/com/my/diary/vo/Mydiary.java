package com.my.diary.vo;

public class Mydiary {
	
	private int mydiary_idx=0;
	private int user_idx=0;
	private String date=null;
	private String weather=null;
	private String nation=null;
	private String title=null;
	private String abroad=null;
	private String location=null;
	private String text=null;
	private String main_img=null;
	
	public int getMydiary_idx() {
		return mydiary_idx;
	}
	public void setMydiary_idx(int mydiary_idx) {
		this.mydiary_idx = mydiary_idx;
	}
	
	public int getUser_idx() {
		return user_idx;
	}
	public void setUser_idx(int user_idx) {
		this.user_idx = user_idx;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getWeather() {
		return weather;
	}
	public void setWeather(String weather) {
		this.weather = weather;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getAbroad() {
		return abroad;
	}
	public void setAbroad(String abroad) {
		this.abroad = abroad;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getMain_img() {
		return main_img;
	}
	public void setMain_img(String main_img) {
		this.main_img = main_img;
	}
	
}
