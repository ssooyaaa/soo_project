package com.my.imjob_clone.vo;

public class Project {

	private int project_idx=0;
	private int newWork_idx=0;
	private String title=null;
	private String field=null;
	private String detail_field=null;
	private String location=null;
	private String type=null;
	private String duration=null;
	private String price=null;
	private String content=null;
	
	
	public int getProject_idx() {
		return project_idx;
	}
	public void setProject_idx(int project_idx) {
		this.project_idx = project_idx;
	}
	
	public int getNewWork_idx() {
		return newWork_idx;
	}
	public void setNewWork_idx(int newWork_idx) {
		this.newWork_idx = newWork_idx;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	public String getDetail_field() {
		return detail_field;
	}
	public void setDetail_field(String detail_field) {
		this.detail_field = detail_field;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
